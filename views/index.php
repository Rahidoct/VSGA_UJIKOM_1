    <h4>Beasiswa Akademik</h4>
    <p>
        Beasiswa Akademik ditujukan untuk siswa yang memiliki prestasi akademik tinggi.
        Beasiswa ini memiliki beberapa ketentuan syarat minimal, termasuk:
    </p>
    <ul>
        <li>IPK minimum 3.25</li>
        <li>Lulusan SMA/sederajat</li>
    </ul>

    <h4>Beasiswa Non-Akademik</h4>
    <p>
        Beasiswa Non-Akademik adalah pilihan bagi siswa yang berperan aktif dalam kegiatan
        sosial dan memiliki motivasi untuk berkontribusi pada masyarakat. Syarat minimal
        untuk beasiswa ini adalah:
    </p>
    <ul>
        <li>Keterlibatan aktif dalam kegiatan sosial atau kegiatan mahasiswa</li>
        <li>Motivasi yang kuat untuk berkontribusi pada masyarakat</li>
    </ul>
